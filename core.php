<?php

    function sum($num1 , $num2){
        return $num1 + $num2;
    }

    function sub($num1 , $num2){
        return $num1 - $num2;
    }

    function mul($num1 , $num2){
        return $num1 * $num2;
    }

    function divide($num1 , $num2){
        return $num1 / $num2;
    }

    function square($num1 , $num2){
        return $num1 ** $num2;
    }